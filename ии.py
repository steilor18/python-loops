# –®–∞–≥ 1: –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –±–∏–±–ª–∏–æ—Ç–µ–∫–∏
from sklearn.linear_model import LinearRegression
import numpy as np

# –®–∞–≥ 2: –î–∞–Ω–Ω—ã–µ –¥–ª—è –æ–±—É—á–µ–Ω–∏—è
# –†–∞–∑–º–µ—Ä –æ–±—É–≤–∏
shoe_size = np.array([36, 38, 40, 42, 44, 37, 39, 41, 43, 45]).reshape(-1, 1)
# –†–æ—Å—Ç –≤ —Å–º
height = np.array([160, 165, 170, 178, 185, 162, 168, 175, 180, 188])

print("üìä –ù–∞—à–∏ –¥–∞–Ω–Ω—ã–µ:")
for i in range(len(shoe_size)):
    print(f"–†–∞–∑–º–µ—Ä –æ–±—É–≤–∏: {shoe_size[i][0]} ‚Üí –†–æ—Å—Ç: {height[i]} —Å–º")

# –®–∞–≥ 3: –°–æ–∑–¥–∞—ë–º AI –º–æ–¥–µ–ª—å
model = LinearRegression()

# –®–∞–≥ 4: –û–ë–£–ß–ê–ï–ú –º–æ–¥–µ–ª—å (–æ–Ω–∞ –∏—â–µ—Ç –∑–∞–∫–æ–Ω–æ–º–µ—Ä–Ω–æ—Å—Ç—å!)
print("\nüß† –û–±—É—á–∞—é –º–æ–¥–µ–ª—å...")
model.fit(shoe_size, height)
print("‚úÖ –ú–æ–¥–µ–ª—å –æ–±—É—á–µ–Ω–∞!")

# –®–∞–≥ 5: –î–µ–ª–∞–µ–º –ø—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–∏–µ
print("\nüîÆ –î–∞–≤–∞–π –ø—Ä–µ–¥—Å–∫–∞–∂–µ–º —Ä–æ—Å—Ç!")
new_shoe_size = np.array([[40]])  # —Ä–∞–∑–º–µ—Ä –æ–±—É–≤–∏ 40
predicted_height = model.predict(new_shoe_size)

print(f"–†–∞–∑–º–µ—Ä –æ–±—É–≤–∏: 40")
print(f"–ü—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–Ω—ã–π —Ä–æ—Å—Ç: {predicted_height[0]:.1f} —Å–º")

# –ü—Ä–æ–≤–µ—Ä–∏–º –µ—â—ë –Ω–µ—Å–∫–æ–ª—å–∫–æ —Ä–∞–∑–º–µ—Ä–æ–≤
print("\nüìà –ü—Ä–µ–¥—Å–∫–∞–∑–∞–Ω–∏—è –¥–ª—è —Ä–∞–∑–Ω—ã—Ö —Ä–∞–∑–º–µ—Ä–æ–≤:")
for size in [35, 38, 41, 44, 47]:
    pred = model.predict([[size]])
    print(f"–†–∞–∑–º–µ—Ä {size} ‚Üí –†–æ—Å—Ç: {pred[0]:.1f} —Å–º")